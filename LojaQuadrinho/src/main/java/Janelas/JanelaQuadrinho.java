    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        if (HQ.verificarArquivo()) {
            HQ.setHqs((List<Quadrinho>) HQ.getObjeto());
        }
        HQ.setValidar(1); JanelaInicial.criaThread(janelaQuadrinhos, true);
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        if (HQ.verificarArquivo()) {
            HQ.setHqs((List<Quadrinho>) HQ.getObjeto());
        }
        janelaQuadrinhos.setPos(HQ.busca(HQ.buscador("Quadrinho")));
        janelaQuadrinhos.setCampos(); HQ.setValidar(2);
        JanelaInicial.criaThread(janelaQuadrinhos, true);
    }//GEN-LAST:event_btEditarActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        if (HQ.verificarArquivo()) {
            HQ.setHqs((List<Quadrinho>) HQ.getObjeto());
        }
        HQ.busca(HQ.buscador("Quadrinho"));
        janelaQuadrinhos.setCampos(); HQ.setValidar(3);
        JanelaInicial.criaThread(janelaQuadrinhos, true);
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (HQ.verificarArquivo()) {
            HQ.setHqs((List<Quadrinho>) HQ.getObjeto());
        }
        HQ.exclui(HQ.busca(HQ.buscador("Editora"))); janelaQuadrinhos.zerarCampos();
        HQ.mensagens(3);
    }//GEN-LAST:event_btExcluirActionPerformed

