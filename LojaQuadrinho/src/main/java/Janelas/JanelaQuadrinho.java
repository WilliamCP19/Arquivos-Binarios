    public final static JanelaQuadrinho getJanelaQuadrinho (String prop) {
        if (janelaQuadrinho == null) {
            janelaQuadrinho = new JanelaQuadrinho ();
        }
        if (prop != null) {
            janelaQuadrinho.HQ.preencheArqProp(janelaQuadrinho.HQ, prop, "Quadrinhos");
            if (janelaQuadrinho.HQ.ler()) {
                janelaQuadrinho.HQ.setHqs((List<Quadrinho>) janelaQuadrinho.HQ.getObjeto());
            }
        }
        return janelaQuadrinho;
    }
    
